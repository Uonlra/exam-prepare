#include <bits/stdc++.h>
using namespace std;

class Solution {
public:
    // 函数功能：在一个整数数组中，找到那个只出现一次的元素
    // 题目前提：其他元素都恰好出现两次
    int singleNumber(vector<int>& nums)
    {
        // ans 作为异或累积的结果，初始为 0
        int ans = 0;

        // 遍历数组中的每一个元素 v
        for (int v : nums)
        {
            // 用异或把当前元素 v “加入” 到结果中
            // 利用的关键性质：
            // 1) x ^ x = 0            （相同的数异或为 0）
            // 2) x ^ 0 = x            （任何数和 0 异或还是它本身）
            // 3) 异或满足交换律、结合律：顺序无关
            //
            // 因此：
            //   数组中出现两次的数，比如 a，最终会变成 a ^ a = 0，被抵消掉；
            //   只有那个出现一次的数，因为没有“配对”被抵消掉，
            //   所以最后会留在 ans 中。
            ans ^= v;
        }

        // 遍历结束后，所有成对出现的数都相互抵消掉，
        // 只出现一次的那个数就是最终的 ans
        return ans;
    }
};